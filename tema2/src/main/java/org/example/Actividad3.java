package org.example;

public class Actividad3 {

        public void Actividad () {
            // Recorremos los números del 1 al 50
            for (int i = 1; i <= 50; i++) {
                // Verificamos si el número es múltiplo de 3
                if (i % 3 == 0) {
                    System.out.println(i);
                }
            }
        }
    }
